package com.mcredit.model.object.warehouse;


public class WareHouseMatrix {
    public String description;
    public String expectedRuleOutput;
    public String actionType;
    public String action;
    public String url;
    public String type;
    public String typeErr;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getExpectedRuleOutput() {
        return expectedRuleOutput;
    }

    public void setExpectedRuleOutput(String expectedRuleOutput) {
        this.expectedRuleOutput = expectedRuleOutput;
    }

    public String getActionType() {
        return actionType;
    }

    public void setActionType(String actionType) {
        this.actionType = actionType;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

	public String getTypeErr() {
		return typeErr;
	}

	public void setTypeErr(String typeErr) {
		this.typeErr = typeErr;
	}   
    
    

}
