package com.mcredit.model.object.warehouse;

public class RemainDocument {
	
	private String employeeLoginId;
	private String employeeFullName;
	private Integer remainDocumentNumber;
	
	public String getEmployeeLoginId() {
		return employeeLoginId;
	}
	public void setEmployeeLoginId(String employeeLoginId) {
		this.employeeLoginId = employeeLoginId;
	}
	public String getEmployeeFullName() {
		return employeeFullName;
	}
	public void setEmployeeFullName(String employeeFullName) {
		this.employeeFullName = employeeFullName;
	}
	public Integer getRemainDocumentNumber() {
		return remainDocumentNumber;
	}
	public void setRemainDocumentNumber(Integer remainDocumentNumber) {
		this.remainDocumentNumber = remainDocumentNumber;
	}
}
